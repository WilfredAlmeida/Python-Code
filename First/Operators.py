# Arithmetic Operators

print("Addition: \n2 + 3 =  ", 2 + 3)  # Addition
print("Subtraction: \n7 - 4 = ", 7 - 4)  # Subtraction
print("Multiplication: \n3 * 4 = ", 3 * 4)  # Multiplication
print("Division: \n4 / 2 = ", 4 / 2)  # Division
print("Integer Division: \n 5 // 2 = ", 5 // 2)  # Integer Division
print("Modulo:\n125 % 5 = ", 125 % 5)  # Modulo
print("Exponent:\n 2**3 = ", 2 ** 3)  # Exponent

# Logical Operators

print("Logical AND:\n (8>9) and (2<9): ", "True" if 8 > 9 and 2 < 9 else "False")
print("Logical OR:\n (2==2) or (9<20): ", "True" if 2 == 2 or 9 < 20 else "False")
print("Logical NOT:\n not(8>2): ", "True" if not (8 > 2) else "False")

# Bitwise Operators

print("Bitwise AND:\n 10&4 = ", (10 & 4))
print("Bitwise OR:\n 10|4 = ", (10 | 4))
print("Bitwise NOT:\n ~10 = ", (~10))
print("Bitwise XOR:\n 10^4 = ", (10 ^ 4))
print("Bitwise RIGHT SHIFT:\n 10>>4 = ", (10 >> 4))
print("Bitwise LEFT:\n 10<<4 = ", (10 << 4))
